func TestValidateSudoersInput(t *testing.T) {
	tests := []struct {
		name      string
		username  string
		cmds      []string
		wantErr   bool
		errSubstr string
	}{
		{
			name:     "valid username and commands",
			username: "runner",
			cmds:     []string{"/usr/bin/kill", "/usr/bin/iptables"},
		},
		{
			name:      "username with space",
			username:  "bad user",
			cmds:      nil,
			wantErr:   true,
			errSubstr: "username",
		},
		{
			name:      "username with semicolon",
			username:  "user;rm",
			cmds:      nil,
			wantErr:   true,
			errSubstr: "username",
		},
		{
			name:      "username with backtick",
			username:  "user`id`",
			cmds:      nil,
			wantErr:   true,
			errSubstr: "username",
		},
		{
			name:      "username with newline",
			username:  "user\nroot ALL=(ALL) ALL",
			cmds:      nil,
			wantErr:   true,
			errSubstr: "username",
		},
		{
			name:      "username with hash",
			username:  "user#comment",
			cmds:      nil,
			wantErr:   true,
			errSubstr: "username",
		},
		{
			name:      "command with space",
			username:  "runner",
			cmds:      []string{"/usr/bin/my command"},
			wantErr:   true,
			errSubstr: "command",
		},
		{
			name:      "command with semicolon",
			username:  "runner",
			cmds:      []string{"/usr/bin/kill;rm -rf /"},
			wantErr:   true,
			errSubstr: "command",
		},
		{
			name:      "empty username returns error",
			username:  "",
			cmds:      []string{"/usr/bin/kill"},
			wantErr:   true,
			errSubstr: "empty",
		},
		{
			name:     "empty commands slice is valid",
			username: "runner",
			cmds:     nil,
		},
	}

	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			err := validateSudoersInput(tt.username, tt.cmds)
			if tt.wantErr {
				if err == nil {
					t.Fatal("expected error, got nil")
				}
				if !strings.Contains(err.Error(), tt.errSubstr) {
					t.Errorf("error %q should contain %q", err.Error(), tt.errSubstr)
				}
			} else {
				if err != nil {
					t.Fatalf("unexpected error: %v", err)
				}
			}
		})
	}
}

func TestBuildSudoersConfig(t *testing.T) {
	tests := []struct {
		name         string
		cfg          *SudoLockdownConfig
		username     string
		wantErr      bool
		wantContains []string
		wantAbsent   string
	}{
		{
			name: "single allowed command",
			cfg: &SudoLockdownConfig{
				AllowCommands: []string{"/usr/sbin/iptables"},
			},
			username: "runner",
			wantContains: []string{
				"/usr/sbin/iptables",
				"runner ALL=(ALL) NOPASSWD:",
			},
		},
		{
			name: "multiple allowed commands comma-separated",
			cfg: &SudoLockdownConfig{
				AllowCommands: []string{"/usr/sbin/iptables", "/usr/sbin/ip6tables"},
			},
			username: "runner",
			wantContains: []string{
				"runner ALL=(ALL) NOPASSWD: /usr/sbin/iptables, /usr/sbin/ip6tables",
			},
		},
		{
			name:     "empty allow commands produces no grant line",
			cfg:      &SudoLockdownConfig{},
			username: "runner",
			wantContains: []string{
				"# CargoWall sudo lockdown configuration",
			},
			wantAbsent: "NOPASSWD",
		},
		{
			name: "unsafe username returns error",
			cfg: &SudoLockdownConfig{
				AllowCommands: []string{"/usr/sbin/iptables"},
			},
			username: "bad;user",
			wantErr:  true,
		},
		{
			name: "output format matches expected sudoers structure",
			cfg: &SudoLockdownConfig{
				AllowCommands: []string{"/usr/sbin/iptables"},
			},
			username: "ci",
			wantContains: []string{
				"# CargoWall sudo lockdown configuration",
				"# Generated by cargowall - DO NOT EDIT",
				"ci ALL=(ALL) NOPASSWD: /usr/sbin/iptables\n",
			},
		},
		{
			name: "header mentions disabled files",
			cfg: &SudoLockdownConfig{
				AllowCommands: []string{"/usr/sbin/iptables"},
			},
			username: "runner",
			wantContains: []string{
				disabledSuffix,
				"sudo-granting groups",
			},
		},
	}

	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			got, err := buildSudoersConfig(tt.cfg, tt.username)
			if tt.wantErr {
				if err == nil {
					t.Fatal("expected error, got nil")
				}
				return
			}
			if err != nil {
				t.Fatalf("unexpected error: %v", err)
			}

			for _, s := range tt.wantContains {
				if !strings.Contains(got, s) {
					t.Errorf("output should contain %q\ngot:\n%s", s, got)
				}
			}

			if tt.wantAbsent != "" && strings.Contains(got, tt.wantAbsent) {
				t.Errorf("output should NOT contain %q\ngot:\n%s", tt.wantAbsent, got)
			}
		})
	}
}

func TestSudoersFileGrantsUser(t *testing.T) {
	tests := []struct {
		name     string
		content  string
		username string
		want     bool
		wantErr  bool
	}{
		{
			name:     "direct grant matches",
			content:  "runner ALL=(ALL) NOPASSWD:ALL\n",
			username: "runner",
			want:     true,
		},
		{
			name:     "grant with tab separator",
			content:  "runner\tALL=(ALL) NOPASSWD:ALL\n",
			username: "runner",
			want:     true,
		},
		{
			name:     "comment line ignored",
			content:  "# runner ALL=(ALL) NOPASSWD:ALL\n",
			username: "runner",
			want:     false,
		},
		{
			name:     "different user no match",
			content:  "admin ALL=(ALL) NOPASSWD:ALL\n",
			username: "runner",
			want:     false,
		},
		{
			name:     "group grant matches when GIDs unknown (fail-closed)",
			content:  "%sudo ALL=(ALL:ALL) ALL\n",
			username: "runner",
			want:     true, // nil GIDs = any %group is a potential grant
		},
		{
			name:     "empty file",
			content:  "",
			username: "runner",
			want:     false,
		},
		{
			name:     "username as substring does not match",
			content:  "runner2 ALL=(ALL) ALL\n",
			username: "runner",
			want:     false,
		},
		{
			name: "match among multiple lines",
			content: `# Defaults
Defaults env_reset
runner ALL=(ALL) NOPASSWD:ALL
`,
			username: "runner",
			want:     true,
		},
		{
			name:     "User_Alias with target user matches",
			content:  "User_Alias RUNNERS = runner, admin\nRUNNERS ALL=(ALL) ALL\n",
			username: "runner",
			want:     true,
		},
		{
			name:     "User_Alias without target user does not match",
			content:  "User_Alias ADMINS = admin, root\nADMINS ALL=(ALL) ALL\n",
			username: "runner",
			want:     false,
		},
		{
			name:     "User_Alias single user",
			content:  "User_Alias CI = runner\nCI ALL=(ALL) NOPASSWD:ALL\n",
			username: "runner",
			want:     true,
		},
		{
			name:     "User_Alias with inline comment",
			content:  "User_Alias CI = runner # CI user\nCI ALL=(ALL) ALL\n",
			username: "runner",
			want:     true,
		},
		{
			name:     "hash include directive returns error",
			content:  "#include /etc/sudoers.d/extra\n",
			username: "runner",
			wantErr:  true,
		},
		{
			name:     "hash includedir directive returns error",
			content:  "#includedir /etc/sudoers.d/extra.d\n",
			username: "runner",
			wantErr:  true,
		},
		{
			name:     "at-include directive returns error",
			content:  "@include /etc/sudoers.d/extra\n",
			username: "runner",
			wantErr:  true,
		},
		{
			name:     "at-includedir directive returns error",
			content:  "@includedir /etc/sudoers.d/extra.d\n",
			username: "runner",
			wantErr:  true,
		},
		{
			name:     "Defaults !authenticate returns error",
			content:  "Defaults !authenticate\n",
			username: "runner",
			wantErr:  true,
		},
		{
			name:     "Defaults with authenticate negation in context returns error",
			content:  "Defaults:runner !authenticate\n",
			username: "runner",
			wantErr:  true,
		},
		{
			name:     "group grant matches when GIDs nil (fail-closed)",
			content:  "%ops ALL=(ALL) ALL\n",
			username: "runner",
			want:     true, // nil GIDs = treat any %group as potential grant
		},
	}

	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			path := filepath.Join(t.TempDir(), "sudoers")
			if err := os.WriteFile(path, []byte(tt.content), 0o644); err != nil {
				t.Fatal(err)
			}
			got, err := sudoersFileGrantsUser(path, tt.username, nil, nil)
			if tt.wantErr {
				if err == nil {
					t.Fatal("expected error, got nil")
				}
				return
			}
			if err != nil {
				t.Fatalf("unexpected error: %v", err)
			}
			if got != tt.want {
				t.Errorf("sudoersFileGrantsUser() = %v, want %v", got, tt.want)
			}
		})
	}
}

